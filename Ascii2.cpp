#include <iostream>
using namespace std;

void drawTree() {
    cout << "        *        " << endl;
    cout << "       ***       " << endl;
    cout << "      *****      " << endl;
    cout << "     *******     " << endl;
    cout << "    *********    " << endl;
    cout << "   ***********   " << endl;
    cout << "  *************  " << endl;
    cout << " *************** " << endl;
    cout << "        ||       " << endl;
    cout << "        ||       " << endl;
}

int main() {
    cout << "ASCII Art: Christmas Tree" << endl;
    drawTree();
    return 0;
}
